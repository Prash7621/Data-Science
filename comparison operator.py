# Comparison operator

# 1. Equal
# 2. Not Equal
# 3. Greater than
# 4. Less than
# 5. Greater than or equal to
# 6. Less than or equal to

# 1. Equal
print(5 == 5)  # True
print(5 == 3)  # False

# 2. Not Equal
print(5 != 5)  # False
print(5 != 3)  # True

# 3. Greater than
print(5 > 3)  # True
print(5 > 5)  # False

# 4. Less than
print(5 < 3)  # False
print(5 < 5)  # False

# 5. Greater than or equal to
print(5 >= 3)  # True
print(5 >= 5)  # True

# 6. Less than or equal to
print(5 <= 3)  # False
print(5 <= 5)  # True
