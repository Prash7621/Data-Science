# logical operator are used to combine conditional statements :

# 1. and operator :  returns true if both statements are true
x=5
print("and operatoe : " , x>3 and x<10) # true

# 2. or operator : return true if one of the statements is true
x=5
print("or operator : " , x>3 or x<4)

# 3. not operator : reverse the result , returns false if the result is true 
x=5
print("not operator: " , not(x>3 and x<10))